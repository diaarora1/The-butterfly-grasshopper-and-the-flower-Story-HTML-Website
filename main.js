function darkmode(){
    document.getElementById("q").style.backgroundColor="black";
    document.getElementById("q").style.color="white";
    document.getElementById("t1").style.color="pink";
    document.getElementById("pic1").style.borderColor="purple";
}

function lightmode(){
    document.getElementById("q").style.backgroundColor="white";
    document.getElementById("q").style.color="black";
    document.getElementById("t1").style.color="rgb(255, 107, 181)";
    document.getElementById("pic1").style.borderColor="rgb(221, 160, 221)";
}